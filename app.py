def build_matrix_3x3(scores: dict, col_scores: dict):
    """
    –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç –º–∞—Ç—Ä–∏—Ü—É 3x3:
    rows: [
      {"row": 1, "perception": "...", "motivation": "...", "instrument": "..."},
      {"row": 2, ...},
      {"row": 3, ...},
    ]
    –õ–æ–≥–∏–∫–∞: —Ç–æ–ø-3 –ø–æ –∫–∞–∂–¥–æ–π –∫–æ–ª–æ–Ω–∫–µ –∏–∑ col_scores, —Ä—è–¥—ã = 1/2/3 –º–µ—Å—Ç–æ –≤ –∫–æ–ª–æ–Ω–∫–µ.
    –ü–ª—é—Å –ø–æ–ø—ã—Ç–∫–∞ –º–∏–Ω–∏–º–∏–∑–∏—Ä–æ–≤–∞—Ç—å –ø–æ–≤—Ç–æ—Ä—ã –∫–∞–º–Ω–µ–π –≤ –ø—Ä–µ–¥–µ–ª–∞—Ö –≤—Å–µ–π –º–∞—Ç—Ä–∏—Ü—ã.
    """

    # col_scores –æ–∂–∏–¥–∞–µ–º –≤–∏–¥–∞: {"perception": {"–ê–º–µ—Ç–∏—Å—Ç": 1.2, ...}, "motivation": {...}, "instrument": {...}}
    per_list = top_n_from_map((col_scores or {}).get("perception", {}), n=3)
    mot_list = top_n_from_map((col_scores or {}).get("motivation", {}), n=3)
    ins_list = top_n_from_map((col_scores or {}).get("instrument", {}), n=3)

    # fallback –µ—Å–ª–∏ –≤–¥—Ä—É–≥ –∫–æ–ª–æ–Ω–∫–∞ –ø—É—Å—Ç–∞—è ‚Äî –±–µ—Ä—ë–º –∏–∑ –æ–±—â–∏—Ö scores
    overall = [p for p, v in sorted((scores or {}).items(), key=lambda x: float(x[1]), reverse=True) if float(v) > 0]
    def fill_fallback(lst):
        for p in overall:
            if p not in lst:
                lst.append(p)
            if len(lst) >= 3:
                break
        while len(lst) < 3:
            lst.append("‚Äî")
        return lst[:3]

    per_list = fill_fallback(per_list)
    mot_list = fill_fallback(mot_list)
    ins_list = fill_fallback(ins_list)

    # --- –£–Ω–∏–∫–∞–ª–∏–∑–∞—Ü–∏—è (–ø–æ –≤–æ–∑–º–æ–∂–Ω–æ—Å—Ç–∏) ---
    used = set()

    def pick(lst, idx):
        # —Å–Ω–∞—á–∞–ª–∞ –ø—Ä–æ–±—É–µ–º –≤–∑—è—Ç—å lst[idx], –µ—Å–ª–∏ –µ—â—ë –Ω–µ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω
        cand = lst[idx] if idx < len(lst) else "‚Äî"
        if cand != "‚Äî" and cand not in used:
            used.add(cand)
            return cand

        # –∏–Ω–∞—á–µ –∏—â–µ–º –≤ —Å–ø–∏—Å–∫–µ –ª—é–±–æ–π –Ω–µ–∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–Ω—ã–π
        for p in lst:
            if p != "‚Äî" and p not in used:
                used.add(p)
                return p

        # –∏–Ω–∞—á–µ –ø—Ä–æ—Å—Ç–æ –≤–æ–∑–≤—Ä–∞—â–∞–µ–º –∫–∞–∫ –µ—Å—Ç—å (–≤—ã–Ω—É–∂–¥–µ–Ω–Ω–æ –ø–æ–≤—Ç–æ—Ä)
        return cand

    rows = []
    for i in range(3):
        rows.append({
            "row": i + 1,
            "perception": pick(per_list, i),
            "motivation": pick(mot_list, i),
            "instrument": pick(ins_list, i),
        })

    return {
        "columns": ["perception", "motivation", "instrument"],
        "labels": {
            "perception": "–í–æ—Å–ø—Ä–∏—è—Ç–∏–µ",
            "motivation": "–ú–æ—Ç–∏–≤–∞—Ü–∏—è",
            "instrument": "–ò–Ω—Å—Ç—Ä—É–º–µ–Ω—Ç",
        },
        "rows": rows
    }

